def _norm_label(lbl: str) -> str:
    return (lbl or "").strip().lower().replace("-", "_")


def style_for(lbl: str) -> Tuple[str, str, float, str]:
    """Returns (marker, linestyle, linewidth, color) for a given label."""
    key = _norm_label(lbl)
    marker = MARKER_MAP.get(key)
    linestyle = LINESTYLE_MAP.get(key)
    linewidth = LINEWIDTH_MAP.get(key, 3.0)
    color = COLOR_MAP.get(key)
    if marker is None or linestyle is None or color is None:
        # Deterministic fallback based on label hash
        idx = abs(hash(key))
        if marker is None:
            marker = _MARKERS_CYCLE[idx % len(_MARKERS_CYCLE)]
        if linestyle is None:
            linestyle = _STYLES_CYCLE[idx % len(_STYLES_CYCLE)]
        if color is None:
            color = _COLORS_CYCLE[idx % len(_COLORS_CYCLE)]
    return marker, linestyle, linewidth, color


def load_records(csv_path: str):
    recs = []
    with open(csv_path, newline="") as f:
        rd = csv.DictReader(f)
        for r in rd:
            try:
                item = {
                    "transport": r["transport"],
                    "payload": int(r["payload"]),
                    "rate": int(r["rate"]),
                    "sub_tps": float(r.get("sub_tps", "")) if r.get("sub_tps", "") else float("nan"),
                    "p50_ms": float(r.get("p50_ms", "")) if r.get("p50_ms", "") else float("nan"),
                    "p95_ms": float(r.get("p95_ms", "")) if r.get("p95_ms", "") else float("nan"),
                    "p99_ms": float(r["p99_ms"]) if r["p99_ms"] else float("nan"),
                    "max_cpu": float(r.get("max_cpu_perc", "") or float("nan")),
                    "max_mem_perc": float(r.get("max_mem_perc", "") or float("nan")),
                    "max_mem_bytes": float(r.get("max_mem_used_bytes", "") or float("nan")),
                    "avg_cpu": float(r.get("avg_cpu_perc", "") or float("nan")),
                    "avg_cpu_cores": float(r.get("avg_cpu_perc", "") or float("nan")) / 100.0 if r.get("avg_cpu_perc", "") else float("nan"),
                    "avg_mem_perc": float(r.get("avg_mem_perc", "") or float("nan")),
                    "avg_mem_bytes": float(r.get("avg_mem_used_bytes", "") or float("nan")),
                    "avg_mem_mb": float(r.get("avg_mem_used_bytes", "") or float("nan")) / (1024.0 * 1024.0) if r.get("avg_mem_used_bytes", "") else float("nan"),
                    "max_mem_mb": float(r.get("max_mem_used_bytes", "") or float("nan")) / (1024.0 * 1024.0) if r.get("max_mem_used_bytes", "") else float("nan"),
                    "connections": int(r.get("connections", "") or 0),
                    "active_connections": int(r.get("active_connections", "") or 0),
                    "run_id": r.get("run_id", ""),
                }
                # Extract pairs 'n<NNN>' from run_id if present
                rid = item.get("run_id", "")
                pairs = None
                rate_per_pub = None
                if rid:
                    m = re.search(r"[_-]n(\d+)(?:[_-]|$)", rid)
                    if m:
                        try:
                            pairs = int(m.group(1))
                        except Exception:
                            pairs = None
                    # Extract rate_per_pub 'r<NNN>' from run_id if present
                    m_rate = re.search(r"[_-]r(\d+)(?:[_-]|$)", rid)
                    if m_rate:
                        try:
                            rate_per_pub = int(m_rate.group(1))
                        except Exception:
                            rate_per_pub = None
                item["pairs"] = pairs
                item["rate_per_pub"] = rate_per_pub
                subs_raw = r.get("subs", "")
                try:
                    item["subs"] = int(subs_raw) if subs_raw else None
                except Exception:
                    item["subs"] = None
                recs.append(item)
            except Exception:
                # Skip malformed rows
                continue
    return recs


def unique_sorted(seq):
    return sorted(set(seq))


def dedupe_by_pairs(records_list: list) -> list:
    """Deduplicate records by pairs count, keeping the last entry for each pairs value.
    
    This handles cases where multiple benchmark runs with the same pairs count
    exist in the summary CSV (e.g., from multiple executions or appending).
    """
    by_pairs = {}
    for r in records_list:
        pairs = r.get("pairs")
        if pairs is not None:
            by_pairs[pairs] = r  # Later entries overwrite earlier ones
    return list(by_pairs.values())


def dedupe_by_subs(records_list: list) -> list:
    """Deduplicate records by subscriber count, keeping the last entry for each subs value."""
    by_subs = {}
    for r in records_list:
        subs = r.get("subs")
        if subs is not None:
            by_subs[subs] = r
    return list(by_subs.values())


def add_legend_top(ax, fig, max_cols: int = 4, reserve_top: float = 0.82, skip_legend: bool = False) -> None:
    """Place a de-duplicated legend at the top of the figure and reserve
    vertical space to avoid overlap with the title.

    reserve_top: fraction of figure height allocated to axes (leave 1-reserve_top for legend).
    skip_legend: if True, skip adding the legend (useful for LaTeX mode on CPU/Memory plots).
    """
    if skip_legend:
        try:
            fig.tight_layout()
        except Exception:
            pass
        return
    handles, labels = ax.get_legend_handles_labels()
    # De-duplicate while preserving order
    seen = set()
    handles2, labels2 = [], []
    for h, lbl in zip(handles, labels):
        if lbl in seen:
            continue
        seen.add(lbl)
        handles2.append(h)
        labels2.append(lbl)
    if not handles2:
        # Still tighten layout even if no legend
        try:
            fig.tight_layout()
        except Exception:
            pass
        return
    ncol = min(len(labels2), max_cols)
    # Reserve space for top legend, then place it
    try:
        fig.tight_layout(rect=(0, 0, 1, reserve_top))
    except Exception:
        pass
    fig.legend(handles2, labels2, loc="upper center", bbox_to_anchor=(0.5, 0.99), ncol=ncol, frameon=False)


def format_pairs_axis(ax, data_xs: list, use_thousands: bool = True) -> None:
    """Format the x-axis for pairs plots.
    
    If use_thousands=True, display values divided by 1000 (e.g., 500->0.5, 1000->1, 2000->2).
    The caller should update the axis label to include '(×1000)'.
    """
    from matplotlib.ticker import FuncFormatter, FixedLocator, NullLocator
    
    def thousands_formatter(x, pos):
        if x <= 0:
            return "0"
        val = x / 1000.0
        # Show as integer if it's a whole number, otherwise one decimal
        if val == int(val):
            return f"{int(val)}"
        else:
            return f"{val:.1f}"
    
    # Set tick locations to actual data points only
    if data_xs:
        unique_xs = sorted(set(data_xs))
        ax.xaxis.set_major_locator(FixedLocator(unique_xs))
        # Disable minor ticks to prevent log scale from adding extra ticks (e.g., 600)
        ax.xaxis.set_minor_locator(NullLocator())
    ax.xaxis.set_major_formatter(FuncFormatter(thousands_formatter))


def format_throughput_axis(ax, rate=None) -> None:
    """Auto-scale throughput Y-axis.

    When `rate` (per-publisher msg/s) is provided the divisor is rate*1000,
    so tick labels read as multiples of 1000× the configured rate — i.e. the
    Y-axis directly shows how many subscriber-thousands were served at the
    configured production rate.  When rate is None the divisor is inferred
    from the plotted data (largest power-of-1000 where max/divisor >= 1).
    """
    import math
    from matplotlib.ticker import FuncFormatter

    if rate is not None and rate > 0:
        divisor = rate * 1000
    else:
        # Determine max y value from all plotted lines
        max_val = 0.0
        for line in ax.get_lines():
            yd = line.get_ydata()
            if len(yd):
                m = max(
                    (float(y) for y in yd if y == y and not math.isinf(float(y))),
                    default=0.0,
                )
                if m > max_val:
                    max_val = m
        if max_val <= 0:
            max_val = 1000.0  # safe fallback
        # Largest power-of-1000 ≤ max_val (minimum 1000)
        exp3 = max(3, int(math.log10(max_val) / 3) * 3)
        divisor = 10 ** exp3

    # Build human-readable annotation
    if divisor == 1_000:
        annotation = "×1000 msg/s"
    elif divisor == 1_000_000:
        annotation = "×10⁶ msg/s"
    elif divisor == 1_000_000_000:
        annotation = "×10⁹ msg/s"
    else:
        annotation = f"×{divisor:,} msg/s"

    def _fmt(x, pos):
        if x <= 0:
            return "0"
        val = x / divisor
        return f"{int(val)}" if val == int(val) else f"{val:.1f}"

    ax.yaxis.set_major_formatter(FuncFormatter(_fmt))
    ax.set_ylabel(f"Throughput\n({annotation})")


def format_memory_mb_axis(ax) -> None:
    """Format the y-axis for memory plots, showing MB values as GB (÷1000)."""
    from matplotlib.ticker import FuncFormatter
    
    def gb_formatter(x, pos):
        if x <= 0:
            return "0"
        val = x / 1000.0  # Convert MB to GB
        # Show as integer if it's a whole number, otherwise one decimal
        if val == int(val):
            return f"{int(val)}"
        else:
            return f"{val:.1f}"
    
    ax.yaxis.set_major_formatter(FuncFormatter(gb_formatter))


def generate_standalone_legend(transports: list, out_dir: str, marker_size: float = 12) -> str:
    """Generate a standalone legend as a separate PDF file.
    
    Creates a horizontal legend with all transport labels and their corresponding
    markers/colors/linestyles, saved as 'legend.pdf' in the output directory.
    
    Returns the filename of the generated legend.
    """
    import matplotlib.pyplot as plt
    from matplotlib.lines import Line2D
    
    # Create custom legend handles for each transport
    handles = []
    labels = []
    for t in sorted(transports):
        m, ls, lw, clr = style_for(t)
        handle = Line2D([0], [0], marker=m, linestyle=ls, linewidth=lw, color=clr,
                       markersize=marker_size, markerfacecolor=clr)
        handles.append(handle)
        labels.append(t)
    
    if not handles:
        return ""
    
    # Create a figure just for the legend
    # Use a wide, short figure to accommodate horizontal legend
    ncol = len(labels)
    fig_width = max(2.0 * ncol, 8)  # Scale width with number of items
    fig, ax = plt.subplots(figsize=(fig_width, 0.8))
    ax.axis('off')
    
    # Add the legend centered in the figure
    legend = fig.legend(handles, labels, loc='center', ncol=ncol, frameon=False,
                       fontsize=LATEX_LEGEND_SIZE, handlelength=2.5, columnspacing=1.5)
    
    # Adjust figure to fit legend tightly
    fig.tight_layout(pad=0.1)
    
    fn = os.path.join(out_dir, "legend.pdf")
    fig.savefig(fn, dpi=300, bbox_inches='tight', pad_inches=0.1)
    fn_png = os.path.join(out_dir, "legend.png")
    fig.savefig(fn_png, dpi=300, bbox_inches='tight', pad_inches=0.1)
    plt.close(fig)
    
    return os.path.basename(fn)


def main() -> int:
    args = parse_args()
    
    if not args.out_dir:
        # Default to a 'plots' directory in the same location as the summary file
        args.out_dir = os.path.join(os.path.dirname(os.path.abspath(args.summary)), "plots")
        print(f"[plot] No --out-dir provided, using default: {args.out_dir}")

    os.makedirs(args.out_dir, exist_ok=True)

    # Matplotlib availability
    try:
        import matplotlib.pyplot as plt  # type: ignore
    except Exception as e:
        print(f"[plot] matplotlib not available: {e}\nInstall with: pip install matplotlib", file=sys.stderr)
        return 0

    # Configure matplotlib for LaTeX mode if requested
    if args.latex:
        plt.rcParams.update({
            'font.size': LATEX_FONT_SIZE,
            'axes.titlesize': LATEX_TITLE_SIZE,
            'axes.labelsize': LATEX_AXIS_LABEL_SIZE,
            'xtick.labelsize': LATEX_FONT_SIZE - 1,
            'ytick.labelsize': LATEX_FONT_SIZE - 1,
            'legend.fontsize': LATEX_LEGEND_SIZE,
            'figure.figsize': LATEX_FIGSIZE,
            'figure.dpi': LATEX_DPI,
            'savefig.dpi': LATEX_DPI,
            'lines.linewidth': LATEX_LINEWIDTH,
            'lines.markersize': LATEX_MARKER_SIZE,
            'axes.linewidth': 0.8,
            'grid.linewidth': 0.5,
            'font.family': 'serif',
            'mathtext.fontset': 'cm',  # Computer Modern for math
        })
        figsize = LATEX_FIGSIZE
        marker_size = LATEX_MARKER_SIZE
        plot_dpi = LATEX_DPI
        plot_ext = ".pdf"  # Vector format for LaTeX
        print(f"[plot] LaTeX mode enabled: {figsize[0]}x{figsize[1]} inches, {plot_dpi} DPI, PDF output")
    else:
        figsize = DEFAULT_FIGSIZE
        marker_size = DEFAULT_MARKER_SIZE
        plot_dpi = 150
        plot_ext = ".png"

    def save_fig(fig, path: str) -> None:
        """Save figure to path; in LaTeX mode also write a .png sibling."""
        fig.savefig(path, dpi=plot_dpi, bbox_inches="tight")
        if args.latex and path.endswith(".pdf"):
            png_path = path[:-4] + ".png"
            fig.savefig(png_path, dpi=plot_dpi, bbox_inches="tight")

    records = load_records(args.summary)
    if not records:
        print("[plot] no records to plot", file=sys.stderr)
        return 0

    payloads = unique_sorted(r["payload"] for r in records)
    transports = unique_sorted(r["transport"] for r in records)

    # Detect latency-only input (no finite sub_tps anywhere)
    latency_only = all(not math.isfinite(rec.get("sub_tps", float("nan"))) for rec in records)

    # For rate-based plots, ignore fanout runs and any rows with non-positive rate or NaN metrics
    if latency_only:
        rate_records = [
            r for r in records
            if not str(r["transport"]).startswith("fanout-") and (r["rate"] or 0) > 0
        ]
    else:
        rate_records = [
            r for r in records
            if not str(r["transport"]).startswith("fanout-")
               and (r["rate"] or 0) > 0
               and math.isfinite(r.get("sub_tps", float("nan")))
        ]
    rates = unique_sorted(r["rate"] for r in rate_records)

    by_pt = defaultdict(list)
    for r in rate_records:
        by_pt[(r["payload"], r["transport"])].append(r)

    # Track images to build a markdown gallery at the end
    throughput_imgs = {}
    p99_imgs = {}
    cpu_imgs = {}
    mem_imgs = {}
    fanout_imgs = {}  # (payload, rate) -> filename
    fanout_cpu_imgs = {}  # (payload, rate) -> filename
    fanout_mem_imgs = {}  # (payload, rate) -> filename
    # New: latency vs payload (for each rate)
    p50_vs_payload_imgs = {}  # rate -> filename
    p95_vs_payload_imgs = {}  # rate -> filename
    p99_vs_payload_imgs = {}  # rate -> filename

    # Throughput vs offered rate (skip if latency-only)
    if not latency_only and not args.only_latency_vs_payload:
        for payload in payloads:
            fig, ax = plt.subplots(figsize=figsize)
            for t in transports:
                xs, ys = [], []
                # Preserve input order of rates on X axis
                for rate in rates:
                    vals = [r for r in by_pt[(payload, t)] if r["rate"] == rate]
                    if not vals:
                        continue
                    sub_tps = vals[0]["sub_tps"]
                    if not math.isfinite(sub_tps):
                        continue
                    xs.append(rate)
                    ys.append(sub_tps)
                if xs and ys:
                    m, ls, lw, clr = style_for(t)
                    ax.plot(xs, ys, marker=m, linestyle=ls, linewidth=lw, color=clr, markersize=marker_size, label=t)
            if not args.latex:
                ax.set_title(f"Throughput vs Offered Rate (payload={payload}B)")
            ax.set_xlabel("Offered rate (msg/s)")
            format_throughput_axis(ax)
            if not args.latex:
                ax.grid(True, alpha=0.4, linestyle="--")
            add_legend_top(ax, fig, skip_legend=args.latex)
            fn = os.path.join(args.out_dir, f"throughput_vs_rate_payload{payload}{plot_ext}")
            save_fig(fig, fn)
            throughput_imgs[payload] = os.path.basename(fn)
            plt.close(fig)

    # Throughput vs Pairs (when run_id includes n<N>)
    # Only meaningful for non-latency-only inputs
    # Generate both log-scale and linear-scale versions
    if not latency_only and not args.only_latency_vs_payload:
        # Group by payload and transport, aggregate by pairs
        by_pt = defaultdict(list)
        for r in records:
            if not math.isfinite(r.get("sub_tps", float("nan"))):
                continue
            if r.get("pairs") is None:
                continue
            by_pt[(r["payload"], r["transport"])].append(r)
        throughput_pairs_imgs = {}
        throughput_pairs_imgs_linear = {}
        for payload in payloads:
            # Collect data once for both plots
            plot_data = {}  # transport -> (xs, ys)
            all_xs = []
            for t in transports:
                lst = by_pt.get((payload, t), [])
                if not lst:
                    continue
                # Deduplicate by pairs count (keep last entry for each pairs value)
                lst = dedupe_by_pairs(lst)
                lst = sorted(lst, key=lambda x: (x.get("pairs") or 0))
                xs = [x.get("pairs") for x in lst if x.get("pairs") is not None]
                ys = [x.get("sub_tps") for x in lst if x.get("pairs") is not None and math.isfinite(x.get("sub_tps", float("nan")))]
                if xs and ys:
                    plot_data[t] = (xs, ys)
                    all_xs.extend(xs)
            
            if not plot_data:
                continue
            
            # Determine rate_per_pub from the data (should be consistent across all records for this payload)
            rate_per_pub_set = set()
            for t, (xs, ys) in plot_data.items():
                for rec in by_pt.get((payload, t), []):
                    if rec.get("rate_per_pub") is not None:
                        rate_per_pub_set.add(rec["rate_per_pub"])
            rate_per_pub_str = ""
            if len(rate_per_pub_set) == 1:
                rate_per_pub_str = f"{list(rate_per_pub_set)[0]} msg/s/pub"
            elif len(rate_per_pub_set) > 1:
                rate_per_pub_str = f"{sorted(rate_per_pub_set)} msg/s/pub"
            
            # Plot 1: Log scale (existing behavior)
            fig, ax = plt.subplots(figsize=figsize)
            for t, (xs, ys) in plot_data.items():
                m, ls, lw, clr = style_for(t)
                ax.plot(xs, ys, marker=m, linestyle=ls, linewidth=lw, color=clr, markersize=marker_size, label=t)
            if not args.latex:
                ax.set_title(f"Throughput vs Pairs ({rate_per_pub_str})" if rate_per_pub_str else "Throughput vs Pairs")
            ax.set_xlabel("Pub-Sub Pairs (×1000)")
            if not args.latex:
                ax.grid(True, alpha=0.4, linestyle="--")
            ax.set_xscale("log")
            format_pairs_axis(ax, all_xs)
            _rate_per_pub = list(rate_per_pub_set)[0] if len(rate_per_pub_set) == 1 else None
            format_throughput_axis(ax, rate=_rate_per_pub)
